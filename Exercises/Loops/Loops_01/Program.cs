// 01.Skriv talen 20 till 30
// Skriv en loop som skriver ut alla tal från 20 till 30.

namespace Loops_01
{
    internal class Program
    {
        static void Main(string[] args)
        {
            for (int i = 20; i <= 30; i++)
            {
                Console.WriteLine(i);
            }
        }
    }
}
